	<p>The decommissioned Sumner Smith Rink found a new life as a temporary dining facility for the Phillips Academy community while Paresky Commons underwent renovations. The rink was transformed into a pleasant dining hall.   The existing outdoor rink was closed in on the sides with a metal sandwich panel system, and four temporary kitchen trailers, with refrigerators, and freezer units were attached to the north façade of the building.  A full service server was built in the rink for front of the house operations.<br><br>Dubbed “UnCommons” by the students, the building seats about 600 in a single room.   Approximately 3,300 meals per day were served out of UnCommons during its use.<br><br>Though the atmosphere is different from that of Commons, which consists of four separate dining halls, students and faculty alike have praised the facility for both its ambiance and the quality of its food service.<br><br>For those students who don’t have time for the extra two-minute walk to UnCommons, another temporary dining solution was provided— a 20-foot custom food-service van dubbed “The Ryley Roller.” The van was parked daily near the Elson Art Center and provided students with a convenient place to grab a free light breakfast or mid-afternoon snack.<br><br></p>


