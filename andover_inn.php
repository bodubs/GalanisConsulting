	<p>On October 1, 2010 Galanis Consulting completed an extensive 15 month renovation  at The Andover Inn, on the campus of Phillips Academy.<br><br>Listed on the National Register of Historic Inns and a campus icon since 1930, the inn is known regionally and nationally for its upscale restaurant and quaint accommodations.  The renovation, made possible by a generous gift, added modern-day amenities to the guestrooms and restaurant, while preserving the inn’s historic character and charm. While the facility has undergone minor improvements over the years, it has never been significantly updated.<br><br>Although a large structure, this Georgian Revival style inn was designed to be domestic in character in order to convey the appearance of a hostelry, yet be compatible with the accepted architectural style for the school at that time. The architect, W. Sydney Wagner, architect of New York’s Roosevelt Hotel, specialized in hotel design while working for George B. Pose & Sons.   Much of Wagner’s original detail is preserved in the lobby and dining areas. The Fredrick Law Olmstead Landscape firm designed the original grounds of the Inn.   The Inn was built on the grounds where Harriet Beacher Stowe’s house first stood.<br><br>Revisions to the 30-room inn included upgrades to both the interior and exterior of the building.  The inn was completely gutted and rehabilitated-with the exception of the main lobby and dining areas, which Phillips Academy wanted to preserve because of the richness of their architectural detailing.  The Inn now features new mechanical and electrical systems, and a new lounge with upgraded kitchen and support area, as well as a new modernized look that will blend contemporary style with the building’s historical nature.</p>

