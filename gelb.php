	<p>Completed in 2004, the Gelb Science Center is a state-of-the-art facility that provides students with an ideal environment to pursue a science curriculum that ranges from introductory courses in biology and chemistry to electives that allow them to explore topics that are rarely encountered in a secondary school science environment.<br><br>This three-story building houses a different discipline on each floor--biology on the first, physics on the second, and chemistry on the third. On the roof is the Gelb Observatory, consisting of a 16-inch reflector telescope housed in an 18 1/2-foot dome. Digital images captured by the telescope are used regularly in classroom and research work by both students and faculty.<br><br>Lab classroom configurations were designed with flexibility to serve both laboratory and traditional class activities. For example, tall chairs are paired with tabletops that double as both desks and lab stations and can be pulled together for larger groups. Electrical outlets and cable wiring for laptop computers are located in every classroom. The World Wide Web, DVDs, VHS and CD-ROMs provide an ever-expanding set of useful materials to enhance science teaching and learning. For daily assignments and research projects, students can link to relevant Web sites, and the material can be displayed in class for clarification, discussion and analysis.<br><br>Computer simulations of various phenomena can be analyzed through the state-of-the-art audiovisual system. Detailed anatomical, cellular, chemical and other diagrams can be rotated, examined, expanded, linked to text or video and otherwise explored through computer applications. Students learn to use digital tools that can record data directly into computers, hence enabling them to read data more quickly, frequently and accurately. Modern scientific instrumentation enhances research conducted in the Gelb Science Center.<br><br></p>


