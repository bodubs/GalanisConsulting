	<p>When the Country Club in Brookline, MA required renovations and restorations to their physical plant in preparation for the 2022 US Open, they called on Galanis Consulting to assist in the process.<br><br>Galanis Consulting was commissioned in 2018 to serve as the Country Club’s owner's representative for a variety of projects and provide the Project Management services for their Capital Improvement Program.<br><br>The Country Club’s Building Committee  recognized the pivotal role of this  historical landmark in preparing for the 2022 US Open and required a firm with past private institutional experience, historical restoration experience, and major construction experience.<br><br>The renovation of Locker Building and Golf Building are the first projects to be performed in this capital program. Total project value approximately $18 million. The Locker Building was constructed in 1915 and designed by the firm Andrews, Jacques and Rantoul. Robert Andrews had been a draftsman under Henry Hobson Richardson before forming his own firm, which designed a number of buildings in Brookline, including Brookline High School in 1895.<br><br>The brick structure was permitted as a “locker house” and originally cost $45,000 to build. Designed in the neo-classical style, the structure features a full height entrance porch with a gabled slate roof supported by columns and a decorative front door surround with a broken pediment. Typical of the style, the façade is symmetrically balanced and supported by a stone foundation. The locker building’s patio was built from funds from the 1963 U.S. Open.<br><br>Galanis Consulting is also assisting The Country Club Facilities Department and Building Committee with future renovations for the post 2022 US Open time period to the Curling Building, and Club House.<br><br>In 2019 to 2020 Galanis Consulting assisted the facilities department in the self performing work as a general contractor for a new 2,700 square foot Laundry Building.<br><br>Work anticipated for 2021 includes renovations to the Grounds Building including a new Carbtrol water recycling system. The project will enhance the mixing program for the grounds fertilization and pesticide management. The Country Club plans to act as the general contractor with Galanis Consulting assistance for this project.<br><br></p>

