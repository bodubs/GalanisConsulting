	<p>Originally built in 1930 for a school enrollment of 150 boys, the Ashburn Chapel required extensive renovations to support a school of 375 boys and girls in 2014. With seating capacity expanded to hold 440 students and faculty the work include the restoration of the pipe organ as well.<br><br>An Aeolian-Skinner Opus 980 built in 1938, the organ has a long and storied history, and preserving and accommodating it was central to the Chapel renovation project. Organs like it are rare, and Brooks’s was given a historical citation by the Organ Historical Society. While it was bought in 1938 with a generous gift from longtime friend of the school Mary Danforth for $7,000, today the instrument is worth $1,000,000. Ernest Skinner, who founded the Aeolian-Skinner Organ Company in 1907, was recognized as one of the finest organ builders of the 20th century.<br><br>The project provided a new entrance featuring handicap access to the basement and Chapel level by building a stairway and elevator lobby addition.  Lowering the basement slab in many areas by more than 2’, and Underpinning of foundation walls allowed the project team to turn unusable crawl space into program space for classrooms, mechanical spaces for the updated M/E/P systems built into the historic building fabric, and bathrooms.<br><br>Construction was managed around the project with close coordination with campus security due to the chapel’s central location on campus.<br><br>Other major changes at the Chapel level included the renovation and the re-orientation of the pews to align with the altar and nave space. In addition to the architectural redesign, a new LED controllable lighting system and a state-of-the-art AV system was installed in the space.<br><br></p>

