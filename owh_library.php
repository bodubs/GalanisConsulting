	<p>Phillips Academy’s first modern library was funded by Thomas Cochran, Class of 1890, who helped orchestrate the physical transformation of campus during that period. Designed by New York architect Charles A. Platt, the OWHL, as it affectionately became known, was named after Holmes Sr., the renowned physician and poet.<br><br>Through the years, Andover’s library has expanded and evolved with the changing needs of its students. The 2019 project transformed the existing library to an integrated learning hub, responsive to the changing ways in which students access information, encouraging new styles of inquiry, learning, research, and collaboration. Design firm Ann Beha Associates has preserved the historic spaces, repurposed support spaces. The former 5 story book stacks were removed, condensed and relocated within the building architecture. They have been replaced by collaborative study and learning spaces, including a global innovations studio. Archival resources are moved to the main floor, giving them a new and accessible presence, joined with Knafel map center. The Sidney R. Knafel Map Collection is history itself in the form of a unique collection of atlases, maps, and globes dating from 1434 well into the 19th century. Many of these rare objects and documents are the subject of a 1992 book, Shedding the Veil: Mapping the European Discovery of America and the World by Thomas Suárez, and comprised in that year an exhibit at the Addison Gallery on the campus of Phillips Academy to mark the 500th anniversary of Columbus’ landing in the New World.<br><br>Restoration of a Stuart Travis Mural took place within the Freeman Room of the OWHL. Travis was an accomplished and prolific American artist, illustrator, and designer who studied at the Académie Julian in Paris. His works—mostly drawings and watercolors—appeared frequently in magazines, books, and advertisements in the early twentieth century.<br><br>This work – along with a new café, makerspace, digital lab, wood shop, 3-D laser printing, robotics, and a 50% increase in seating – create new academic synergies with vision and spirit.<br><br></p>

