	<p>The Memorial Bell Tower was originally designed by Guy Lowell, based upon the steeple of Bostonâ€™s Old South Meeting House.  Built on the historic Training Field where military companies drilled during the American Revolution, the War of 1812, the Civil War, the Spanish-American War and World War I.  It was constructed in the memory of the 85 Andover students who died in World War I.<br><br>The tower was dismantled and rebuilt, in order to correct engineering flaws that had compromised the structural integrity of the building and that ultimately forced the bells into silence in 1989. As much of the original fabric of the building as possible was salvaged and reused for the reconstruction, most notably the entire three-tiered wood belfry that forms the upper half of the structure.<br><br>Though the reconstructed tower appears identical to the original tower built in 1923, right down to the hand cut engravings in the Deer Island granite blocks at the base of the building, the structure has been fitted with an enhanced set of bells that is configured with a touch-sensitive electronic carrilon system. The system features a keyboard on the ground floor that is used to sound the bells, which remain stationary when played. In total, the new tower holds 49 bells, 19 English Bells of which were used in the original tower.  The largest bell, which plays low-E, weighs 2,347 pounds.   A new structural framing for the bell support system was required in order to accommodate the new bells.<br><br></p>


