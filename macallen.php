	<p>The project involved the construction of a new 15 story (inclusive of parking garage) 354,000 sf structural steel residential building that will contain 143 apartments with a first floor open public terrace set on top of a post-tensioned concrete three level parking garage set partially below grade.<br><br>Structural framing for this residential project will utilize a staggered truss framing system with a specialized metal deck and concrete topping.<br><br>The building has been LEED Certified at the Gold level. In 2007 it was the first residential project of it's kind in Boston.<br><br>Some of the highlighted “Green” features of the building that demonstrate the building’s commitment to the environment are:<br><br></p>

	<ul id="mac-list">
		<li>Building central plant – heating and cooling will be more efficient due to economizing the system for the entire building, instead of a heating and cooling system for each unit</li>
		<li>Day lighting – the use of day lighting within the units will provide natural sunlight to heat and light many of the apartments, thus reducing the need for additional lighting and heating loads during these time periods</li>
		<li>Green living roof – the green, living roof will help keep the building cool in the summer and help insulate it in the winter, which in turn can help to reduce heating and cooling loads</li>
		<li>Storm water will be used to irrigate the terrace roof, thus reducing city water usage and the cost of that usage</li>
		<li>Recycled quartz counter tops</li>
		<li>Reuse of storm water for irrigation</li>
		<li>Treatment of the cooling tower water ultrasonically through an award-winning new technique known as the Dolphin System</li>
		<li>Double flush toilet:  The double flush toilet uses approximately 2/3rds the amount of water that a standard low flow toilet uses</li>
		<li>Energy efficient Bosch dishwasher</li>
		<li>Energy efficient lighting - the goal is to provide energy efficient lighting in both the units and the public areas, which should help to reduce the building’s energy load and in turn provide lower condo fees</li>
		<li>Bamboo floors<br><br></li>
	</ul>

